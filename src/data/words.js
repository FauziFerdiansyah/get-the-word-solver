// Helper to get all words (common + rare) for a length
export function getAllWords(length) {
  const list = WORD_LISTS[length];
  if (!list) return [];
  return [...list.common, ...list.rare];
}

// Helper to get unique words (no duplicates between common and rare)
export function getUniqueWords(length, category = 'all') {
  const list = WORD_LISTS[length];
  if (!list) return [];
  
  if (category === 'common') return [...list.common];
  if (category === 'rare') return [...list.rare];
  
  // For 'all', return common + rare (rare words that aren't in common)
  const commonSet = new Set(list.common);
  const uniqueRare = list.rare.filter(w => !commonSet.has(w));
  return [...list.common, ...uniqueRare];
}
